package frc.robot;

public class DriveData {
	
	private float Distance;
	private float Angle;
	
	public float getDistance() {
		return Distance;
	}
	
	public void setDistance(float distance) {
		Distance = distance;
	}
	
	public float getAngle() {
		return Angle;
	}
	
	public void setAngle(float angle) {
		Angle = angle;
	}
}